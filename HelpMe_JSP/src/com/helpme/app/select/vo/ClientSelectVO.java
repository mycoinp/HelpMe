public class ClientSelectVO {
	
	private String cs_helper_id;
	private String cs_select_date;
	private String cs_area;
	private String cs_service;
	private int cs_dayNum;
	
	
	public String getCs_helper_id() {
		return cs_helper_id;
	}
	public void setCs_helper_id(String cs_helper_id) {
		this.cs_helper_id = cs_helper_id;
	}
	public String getCs_select_date() {
		return cs_select_date;
	}
	public void setCs_select_date(String cs_select_date) {
		this.cs_select_date = cs_select_date;
	}
	public String getCs_area() {
		return cs_area;
	}
	public void setCs_area(String cs_area) {
		this.cs_area = cs_area;
	}
	public String getCs_service() {
		return cs_service;
	}
	public void setCs_service(String cs_service) {
		this.cs_service = cs_service;
	}
	public int getCs_dayNum() {
		return cs_dayNum;
	}
	public void setCs_dayNum(int cs_dayNum) {
		this.cs_dayNum = cs_dayNum;
	}
	
}
