package com.helpme.app.client.vo;

public class ClientVO {
	private int client_no;
	private String client_id;
	private String password;
	private String name;
	private String email;
	private String country;
	private String address;
	private String phone_number;
	private int available_points;
	private int cancel_cnt;
	
	public ClientVO() {;}

	public int getClient_no() {
		return client_no;
	}

	public void setClient_no(int client_no) {
		this.client_no = client_no;
	}

	public String getClient_id() {
		return client_id;
	}

	public void setClient_id(String client_id) {
		this.client_id = client_id;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPhone_number() {
		return phone_number;
	}

	public void setPhone_number(String phone_number) {
		this.phone_number = phone_number;
	}

	public int getAvailable_points() {
		return available_points;
	}

	public void setAvailable_points(int available_points) {
		this.available_points = available_points;
	}

	public int getCancel_cnt() {
		return cancel_cnt;
	}

	public void setCancel_cnt(int cancel_cnt) {
		this.cancel_cnt = cancel_cnt;
	}

	



	
	
}
